<h4>INTRODUCTION</h4>
<p>
Dans le cadre de l’UV TN20 (modélisation CAO), nous avons eu à rétro modélisé un système, afin de se confronter aux éventuelles difficultés liées à la modélisation CAO. Pour cela nous allons utiliser deux logiciels CAO que sont CATIA et Créo.<br/>

Contraintes et choix du sujet :<br/>

-nombre de pièce entre 8 et 25 pièces (pièces uniques or standards)<br/>

-minimum de deux pièces en surfacique<br/>

-conception squelette<br/>

-assemblage final sous CATIA avec 70% des pièces sous CATIA et 30% sous Créo<br/>

Pour notre part nous avons choisis de rétro modéliser une ponceuse électrique !
</p>

<h4>LE SYSTEME</h4>
<p>
Le système est composé d’environ 16 pièces, en voici le plan d’ensemble :<br/>

<img src="http://placehold.it/600x400" /><br/>

Fonctionnement :<br/>

Le rotor sous l’effet du champ magnétique produit par le stator et sa propre bobine, va se mettre en rotation et entrainer les différentes pièces rattachées à l’arbre moteur dont le disque excentré qui va faire tourner le roulement qui l’entoure, autour d’un axe différent de l’axe moteur, ce qui va entrainer l’effet de vibration sur la plaque de ponçage.<br/>

Les paliers et disques ventilant vont permettre d’aspirer la poussière produite et de la rejeter par l’arrière de la ponceuse.<br/>
</p>

